// Build the lookup map once
const _scenarios = new Map<string, readonly [string,string,string,string,string,string,string,string]>();
for (const [, key, ...assignments] of RAW) {
  _scenarios.set(key, assignments as [string,string,string,string,string,string,string,string]);
}

/**
 * Given the 8 group letters whose third-place teams advance, return the
 * Round-of-32 bracket assignment for each group-winner match slot.
 *
 * @param advancingGroups  Array of 8 group letters, e.g. ["E","F","G","H","I","J","K","L"]
 * @returns Object mapping each winner slot to its third-place opponent,
 *          e.g. { "1A": "3E", "1B": "3J", ... }, or null if no scenario found.
 */
export function lookupThirdPlaceScenario(
  advancingGroups: string[]
): Record<ThirdPlaceSlot, string> | null {
  const key = [...advancingGroups].sort().join("");
  const assignments = _scenarios.get(key);
  if (!assignments) return null;
  return Object.fromEntries(
    THIRD_PLACE_MATCH_SLOTS.map((slot, i) => [slot, assignments[i]])
  ) as Record<ThirdPlaceSlot, string>;
}

/**
 * Return the scenario number (1-495) for a given set of advancing groups,
 * or null if not found.
 */
export function getScenarioNumber(advancingGroups: string[]): number | null {
  const key = [...advancingGroups].sort().join("");
  for (const [num, k] of RAW) {
    if (k === key) return parseInt(num);
  }
  return null;
}
